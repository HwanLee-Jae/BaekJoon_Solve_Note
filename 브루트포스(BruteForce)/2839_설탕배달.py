#문제
#어떤 수들이 있을 때, 그 수들을 대표하는 값으로 가장 흔하게 쓰이는 것은 평균이다. 평균은 주어진 모든 수의 합을 수의 개수로 나눈 것이다.
#예를 들어 10, 40, 30, 60, 30의 평균은 (10 + 40 + 30 + 60 + 30) / 5 = 170 / 5 = 34가 된다.

#평균 이외의 또 다른 대표값으로 중앙값이라는 것이 있다. 중앙값은 주어진 수를 크기 순서대로 늘어 놓았을 때 가장 중앙에 놓인 값이다. 
#예를 들어 10, 40, 30, 60, 30의 경우, 크기 순서대로 늘어 놓으면

#10 30 30 40 60

#이 되고 따라서 중앙값은 30이 된다.

#다섯 개의 자연수가 주어질 때 이들의 평균과 중앙값을 구하는 프로그램을 작성하시오.

#입력
#첫째 줄부터 다섯 번째 줄까지 한 줄에 하나씩 자연수가 주어진다. 주어지는 자연수는 100 보다 작은 10의 배수이다.

#출력
#첫째 줄에는 평균을 출력하고, 둘째 줄에는 중앙값을 출력한다. 평균과 중앙값은 모두 자연수이다.

#[코드]
# 사용자로부터 목표 숫자 n을 입력받습니다.
n = int(input())

# 5x + 3y = n 조건을 만족하는 x와 y의 합을 저장할 리스트를 초기화합니다.
cnt = []

# x와 y는 각각 5와 3의 배수이므로, 가능한 x와 y의 값을 모두 탐색합니다.
# x의 최대 범위는 1000으로 설정합니다. 이는 현실적인 범위 내에서 적당한 상한선입니다.
for x in range(1001):  # x는 0부터 1000까지 반복
    for y in range(1700):  # y는 0부터 1699까지 반복 (y의 최대 범위도 설정)
        # 5*x + 3*y가 n과 같으면, 조건을 만족하는 (x, y) 쌍을 찾은 것입니다.
        if 5*x + 3*y == n:
            # x와 y의 합을 리스트에 추가합니다.
            cnt.append(x + y)

# cnt 리스트가 비어 있으면, 조건을 만족하는 (x, y) 조합이 없다는 의미이므로 -1을 출력합니다.
if len(cnt) == 0:
    print(-1)
else:
    # cnt 리스트를 오름차순으로 정렬한 후
    # 가장 작은 값을 출력합니다. 이는 가장 적은 x + y 값을 의미합니다.
    cnt.sort()
    print(cnt[0])
