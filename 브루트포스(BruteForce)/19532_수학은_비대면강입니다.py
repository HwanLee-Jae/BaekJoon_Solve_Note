# 사용자로부터 6개의 정수를 입력받습니다.
# 이 값들은 연립 방정식의 계수와 상수입니다.
a, b, c, d, e, f = map(int, input().split())

# 연립 방정식:
# a*x + b*y = c
# d*x + e*y = f

# 위의 두 방정식을 해결하기 위해서, x와 y를 다음과 같이 계산합니다.
# x를 구하기 위한 식:
# x = (c*e - b*f) / (a*e - b*d)
# 이 식은 두 번째 방정식의 계수 e로 첫 번째 방정식의 상수 c를 조정한 후,
# 두 번째 방정식의 상수 f를 첫 번째 방정식의 계수 b로 조정한 값을 빼서 x를 계산합니다.
# 분모는 두 방정식의 계수에서 일차 방정식을 만드는 요소입니다.

# y를 구하기 위한 식:
# y = (c*d - a*f) / (b*d - a*e)
# 이 식은 첫 번째 방정식의 계수 a로 두 번째 방정식의 상수 f를 조정한 후,
# 첫 번째 방정식의 상수 c를 두 번째 방정식의 계수 d로 조정한 값을 빼서 y를 계산합니다.
# 분모는 두 방정식의 계수에서 일차 방정식을 만드는 요소입니다.

# x를 계산합니다.
x = (c * e - b * f) // (a * e - b * d)

# y를 계산합니다.
y = (c * d - a * f) // (b * d - a * e)

# 계산된 x와 y 값을 출력합니다.
# x와 y는 연립 방정식의 해로, 주어진 방정식들을 만족하는 값을 나타냅니다.
print(x, y)
